                    <p>Teota Zamindar Palace (তেওতা জমিদার বাড়ী) is located at the Shibalaya (শিবালায়া) Upazila of Manikganj District. This palace is locally known as Teota Rajbari (তেওতা রাজবাড়ি) and Teota Zamindar Bari (তেওতা জমিদার বাড়ী). This palace is possibly over 300 years old. This old dilapidated palace was the home of two brothers named Babu Hemsankar Ray ( বাবু হেমসংকর রায়) and Babu Joy Sankar Ray (বাবু জয় সংকর রায়) who were the ruler of Teota. The whole premise has over 50 rooms. Currently those are occupied by the homeless people.

At the Western side of the building, there is a large pond and beside the pond a temple is located named as Noborotno Temple (নবরত্ন মন্দির). The temple is a four storied building having a glorious example of classical architecture.

The whole palace is situated just beside the bank of mighty river Jamuna. After a few miles from the palace premise, the river merged with another big river, Padma. The palace is a two storied building and from the roof you can easily see the Jamuna river.

This palace is the birth place of Promila Debi (প্রমীলা দেবী), the wife of our national poet Kazi Nazrul Islam (নজরুল ইসলাম). Poet first saw her in this palace when he came here to visit Promila’s cousin-Dhiren Sen (ধীরেন সেন).</p>
                    
